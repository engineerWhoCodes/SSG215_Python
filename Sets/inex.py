#Showing that implicit and explicit line joining produces the same result
#Implicit line continuations(breaking the commas )
olu = {'eba', 'okro', 'beans', 
'boli', 'rice', 'stew'}

#Explicit line continuations(backlash to create a forced line)
chike = {'rice', 'ugwu', 'akpu', \
    'beans', 'plaintains' \
        'yams', 'agazi'}
print(olu)
print(chike)